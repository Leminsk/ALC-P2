/*
f(x) = c_1 * exp(c_2 * x) + c_3 * x^c_4

*/

double targetF(double &x, double &c_1, double &c_2, double &c_3, double &c_4){
    return (c_1)*exp(c_2*x) + c_3*pow(x, c_4);
}

double dF(double &x, double &c_1, double &c_2, double &c_3, double &c_4){
    return (c_3)*(c_4)*pow(x, c_4 -1) + (c_1)*(c_2)*exp(c_2*x);
}


